function textToMorse(text) {
    var chars = text.split('');
    var ret = '';
    for (var i = 0; i < chars.length; i++) {
        var char = chars[i].toLowerCase();
        if (alphabetToMorse[char]) {
            ret += alphabetToMorse[char] + ' ';
        }
    }
    return ret;
}

function morseToText(morse) {
    var words = morse.split(/\s{3,}|\.{5,7}/);
    for (var i = 0; i < words.length; i++) {
        var word = words[i];
        word = word.replace(/^\s+/, '');
        word = word.replace(/\s+$/, '');
        word = word.replace(/\s+/, ' ');
        words[i] = word;
    }
    var ret = '';
    for (var i = 0; i < words.length; i++) {
        var word = words[i];
        var chars = word.split(' ');
        for (var j = 0; j < chars.length; j++) {
            var char = chars[j];
            if (morseToAlphabet[char]) {
                var letter = morseToAlphabet[char];
            }
            else {
                var letter = '?'
            }
            ret += letter;
        }
        ret += ' ';
    }
    return ret;

}
